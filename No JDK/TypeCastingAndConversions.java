public class TypeCastingAndConversions {
    public static void main(String[] args) {
        double d = 9.78;
        int i = (int) d;
        int a = 5;
        double b = a;
        System.out.println("double d = " + d);
        System.out.println("cast to int i = " + i);
        System.out.println("int a to double b =" + b);
        char c = 65;
        System.out.println("char from int 65 =" + c);
    }
    
}
